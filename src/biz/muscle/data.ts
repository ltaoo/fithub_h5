export const MusclesInPartMaps = Muscles.reduce<Record<string, { muscles: typeof Muscles }>>((acc, muscle) => {
  if (!acc[muscle.part]) {
    acc[muscle.part] = { muscles: [] };
  }
  acc[muscle.part].muscles.push(muscle);
  return acc;
}, {});

export function map_parts_with_ids(ids: number[]) {
  const parts = Muscles.filter((m) => {
    return ids.includes(m.id);
  }).map((m) => {
    return m.part;
  });
  return [...new Set(parts)];
}
